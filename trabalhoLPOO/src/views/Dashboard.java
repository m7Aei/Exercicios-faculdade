    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        cadastro cadastrar = new cadastro();
        cadastroDAO cadastro = new cadastroDAO();
        
        cadastrar.setNome(textNome.getText());
        cadastrar.setIdade(Integer.parseInt(textIdade.getText()));
        cadastrar.setGenero(textGenero.getText());
        cadastrar.setFaixaGraduacao(textCategoria.getText());
        cadastrar.setCidade(textCidade.getText());
        cadastrar.setAcademia(textAcademia.getText());
        cadastrar.setEmail(textEmail.getText());
        cadastrar.setTelefone(textTelefone.getText());

        cadastrar.validarCampos();
        //DashboardLuta lutar = new DashboardLuta();
        //lutar.setVisible(true);
        
        this.setVisible(false);
        cadastro.create(cadastrar);
    }//GEN-LAST:event_jButton1ActionPerformed

